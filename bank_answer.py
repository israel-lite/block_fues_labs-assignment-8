def deposit(self, amount):
        if amount > 0:
            self.balance += amount
            return True
        return False

def withdraw(self, amount):
        if 0 < amount <= self.balance:
            self.balance -= amount
            return True
        return False

def get_balance(self, ):
        return self.balance
